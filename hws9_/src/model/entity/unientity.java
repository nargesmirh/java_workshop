package model.entity;

public class unientity {
    private String name;
    private int code;
    private String capacity;
    private int unit;


    public unientity() {

    }


    public String getName() {
        return name;
    }

    public unientity setName(String name) {
        this.name = name;
        return this;
    }

    public int getCode() {
        return code;
    }

    public unientity setCode(int code) {
        this.code = code;
        return this;
    }

    public String getCapacity() {
        return capacity;
    }

    public unientity setcapacity(String capacity) {
        this.capacity = capacity;
        return this;
    }

    public int getUnit() {
        return unit;
    }

    public unientity setunit(int unit) {
        this.unit = unit;
        return this;
    }


}
